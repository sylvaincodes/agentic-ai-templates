import fs from "fs-extra";
import { marked } from "marked";
import path from "node:path"; // Using node: prefix for Node 25 compatibility
import puppeteer from "puppeteer";
import type { Agent, AgentAdapter, Command, Skill } from "../types/index.js";

export class ClaudeCodeAdapter implements AgentAdapter {
  target = "claude-code" as const;
  private configDir = path.join(process.cwd(), ".claude");

  async installSkill(skill: Skill): Promise<void> {
    // 1. Production Safety: Ensure we are in a writable directory
    try {
      await fs.ensureDir(this.configDir);
    } catch (err) {
      throw new Error(
        `Permission Denied: Cannot create directory at ${this.configDir}`,
      );
    }

    const sanitizedName = skill.name
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "");

    // 2. Nested path support: skill.id (e.g., 'product/roadmap') becomes nested folders
    const skillFolder = path.join(this.configDir, "skills", skill.id);
    const skillFile = path.join(skillFolder, "SKILL.md");
    const metaFile = path.join(skillFolder, "skill.json");

    await fs.ensureDir(skillFolder);

    const skillMarkdown = `
---
name: ${sanitizedName}
description: ${skill.description}
version: ${skill.version}
---

# 🧠 Skill: ${skill.name} (v${skill.version})

> ${skill.description}

## System Prompt
${skill.system_prompt}

## Rules
${skill.rules?.map((r) => `- ${r}`).join("\n")}

## Expected Output Format
${skill.output_format ?? "No specific format required."}
`.trim();

    // 3. Performance: Parallel writing
    await Promise.all([
      fs.writeFile(skillFile, skillMarkdown, "utf-8"),
      fs.writeFile(metaFile, JSON.stringify(skill, null, 2), "utf-8"),
    ]);
  }

  // Implementation for Commands
  async installCommand(command: Command, goalId: string): Promise<void> {
    const commandDir = path.join(this.configDir, "commands");
    await fs.ensureDir(commandDir);

    // Use the trigger name as the filename (e.g., /launch-landing-page -> launch-landing-page.md)
    const fileName = command.trigger.replace("/", "") + ".md";
    const filePath = path.join(commandDir, fileName);

    const commandMarkdown = `
---
name: ${command.trigger.replace("/", "")}
description: ${command.description}
---

# Command: ${command.trigger} ${command.parameters?.map((p) => `[${p}]`).join(" ")}

## Purpose
${command.description}

## Orchestration Workflow
You MUST follow these steps in order. Use the installed skills located in .claude/skills/ to execute each phase.

${command.orchestration.map((step, index) => `${index + 1}. ${step}`).join("\n")}

## Completion Criteria
${command.checklist?.map((item) => `- [ ] ${item}`).join("\n") ?? "- [ ] Verify all steps are complete."}

> This command was generated by Agentic AI Marketplace for Goal: ${goalId}
`.trim();

    await fs.writeFile(filePath, commandMarkdown, "utf-8");
  }

  // Implementation for Agents
  async installAgent(name: string | undefined, agent: Agent): Promise<void> {
    const agentDir = path.join(this.configDir, "agents");
    await fs.ensureDir(agentDir);

    const agentFilePath = path.join(agentDir, `${name}.md`);

    // We reference the Skill markdown as the "Knowledge Base" for this agent
    const skillPath = `.claude/skills/${agent.skill_id}/SKILL.md`;

    const agentMarkdown = `
---
role: ${agent.role}
assigned_skill: ${skillPath}
version: ${agent.version}
type: agent
---

# 👥 Agent Persona: ${agent.role} (@${name})

## Primary Instruction
${agent.instruction}

## Core Knowledge
This agent is powered by the expert rules defined in: \`${skillPath}\`.

## ⚠️ STRICT COMPLIANCE RULES
1. **STRICT SKILL ADHERENCE**: You are FORBIDDEN from using generic AI responses. You MUST read the expert rules in \`${skillPath}\` before acting.
2. **FORMAT LOCK**: You MUST strictly use the "Expected Output Format" defined in the skill file. Do not omit headers or sections.
3. **CORE KNOWLEDGE**: Your intelligence is derived from: \`${skillPath}\`.

## Collaboration Protocol
1. **Context Isolation**: When active, focus exclusively on the ${agent.role} persona.
2. **Handoffs**: Save progress and structured data to the \`output/\` directory for other team members.
3. **Skill Adherence**: Strict adherence to the linked skill's rules is mandatory.

> [!NOTE]
> Provisioned via Agentic AI Marketplace (v${agent.version}).
`.trim();

    await fs.writeFile(agentFilePath, agentMarkdown, "utf-8");
  }

  private slugify(text: string): string {
    return text
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "");
  }

  async remove(skillId: string): Promise<void> {
    const skillFolder = path.join(this.configDir, "skills", skillId);

    // Safety check: Only remove if it's actually within the .claude directory
    if (
      skillFolder.includes(this.configDir) &&
      (await fs.pathExists(skillFolder))
    ) {
      await fs.remove(skillFolder);
    }
  }

  async generatePDF(markdownPath: string, outputPath: string) {
    const markdown = await fs.readFile(markdownPath, "utf-8");
    const htmlContent = await marked(markdown);

    const browser = await puppeteer.launch({ headless: true });
    const page = await browser.newPage();

    // Add your professional styling here
    const styledHtml = `
    <html>
      <head>
        <style>
          body { font-family: sans-serif; padding: 40px; line-height: 1.6; }
          h1 { color: #1a365d; border-bottom: 2px solid #eee; }
          pre { background: #f4f4f4; padding: 15px; border-radius: 8px; }
        </style>
      </head>
      <body>${htmlContent}</body>
    </html>
  `;

    await page.setContent(styledHtml, { waitUntil: "networkidle0" });
    await page.pdf({
      path: outputPath,
      format: "A4",
      margin: { top: "20mm", bottom: "20mm", left: "20mm", right: "20mm" },
      printBackground: true,
    });

    await browser.close();
  }
}
